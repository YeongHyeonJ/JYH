package day4;

public class ForAlphabetEx1 {

	public static void main(String[] args) {
		char ch1 = 'a';
		int num = 1;
		char ch2 = (char)(ch1 + 1);
		char ch3 = 'a' + 1;
		char ch4 = (char)(ch1 + num);
		System.out.println("ch2 : " + ch2);
		System.out.println("ch3 : " + ch3);
		System.out.println("ch4 : " + ch4);
	}

}
